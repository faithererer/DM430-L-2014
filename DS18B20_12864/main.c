#include <msp430x14x.h>
#include "Config.h"
#include "12864.c"
#include "DS18B20.c"

//***********************************************************************
//      主程序
//***********************************************************************
void main(void) 
{
    uchar j;
  
    WDT_Init();                         //看门狗设置
    Clock_Init();                       //系统时钟设置
    Port_init();                        //系统初始化，设置IO口属性
    delay_ms(100);                      //延时100ms
    LCD_init();                         //液晶参数初始化设置
    LCD_clear();                        //清屏
    LCD_Desk();
     
    DS18B20_Reset();			  //复位D18B20
    while(1)
    {
    ds1820_start();		          //启动一次转换
    ds1820_read_temp();		          //读取温度数值
    data_do(temp_value);                  //处理数据，得到要显示的值
    for(j=0;j<30;j++)
    {	    
     LCD_write_str(0x04,2,Temp_Value);    //显示温度，温度值在Temp_Value数组里
    }
   }
    
}

