#include <msp430x14x.h>
#include "Config.h"

//***********************************************************************
//               MSP430IO口初始化
//***********************************************************************
void Port_Init()
{
  LED8DIR = 0xff;                    //设置IO口方向为输出
  LED8 = 0xFF;                       //初始设置为00
}



//***********************************************************************
//      主程序
//***********************************************************************
void main(void)
{ 
  WDT_Init();
  Clock_Init();                         //时钟初始化
  Port_Init();                          //端口初始化，用于控制IO口输入或输出
  while(1)
  {
    
    LED8 |= 1<<0;                      //如果要显示闪烁别的灯，改0为其他就可以（0~7）
                        
    delay_ms(500);
                          
    LED8 &= ~(1<<0);
    
    delay_ms(500);
  }
}



